
public class basics {
    public static void main(String[] args) {
        int a = 5;
        int b = 2;
        System.out.println(a & b);   //binary AND
        System.out.println(a | b);   //binary OR
        System.out.println(a ^ b);   //binary XOR
        System.out.println(~a);      //binary ONE'S COMPLEMENT
        System.out.println(a << b);  //binary LEFT DHIFT
        System.out.println(a >> b);  //binary RIGHT SHIFT
    }
}
